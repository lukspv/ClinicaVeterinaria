public class Servicos {
    
    private int Servid;
    private String Servnome;
    private int Servtemp;
    private double Servval;

    /**
     * @return the Servid
     */
    public int getServid() {
        return Servid;
    }

    /**
     * @param Servid the Servid to set
     */
    public void setServid(int Servid) {
        this.Servid = Servid;
    }

    /**
     * @return the Servnome
     */
    public String getServnome() {
        return Servnome;
    }

    /**
     * @param Servnome the Servnome to set
     */
    public void setServnome(String Servnome) {
        this.Servnome = Servnome;
    }

    /**
     * @return the Servtemp
     */
    public int getServtemp() {
        return Servtemp;
    }

    /**
     * @param Servtemp the Servtemp to set
     */
    public void setServtemp(int Servtemp) {
        this.Servtemp = Servtemp;
    }

    /**
     * @return the Servval
     */
    public double getServval() {
        return Servval;
    }

    /**
     * @param Servval the Servval to set
     */
    public void setServval(double Servval) {
        this.Servval = Servval;
    }
    
    
    
}
